pub fn pick_three() -> [usize; 3] {
    use rand::Rng;
    let mut rng = rand::thread_rng();
    let mut indices = [0usize; 3];
    indices[0] = rng.gen_range(0..TEMPLATES.len());
    loop {
        indices[1] = rng.gen_range(0..TEMPLATES.len());
        if indices[1] != indices[0] { break; }
    }
    loop {
        indices[2] = rng.gen_range(0..TEMPLATES.len());
        if indices[2] != indices[0] && indices[2] != indices[1] { break; }
    }
    indices
}
